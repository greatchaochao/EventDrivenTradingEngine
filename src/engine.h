// #pragma once：头文件保护指令，告诉编译器"这个文件只允许被包含一次"
//
// 问题背景：
//   一个大型项目中，多个 .cpp 文件可能都 #include 同一个头文件
//   如果没有保护，编译器会把头文件的内容复制多次，导致"重复定义"错误
//
// #pragma once 的作用：
//   编译器第一次读到这个文件时正常处理
//   之后若再次遇到 #include "engine.h"，直接跳过，不重复处理
//
// 这是现代 C++ 的标准写法，所有头文件（.h）都应在第一行加上它
#pragma once

// #include 有两种写法，区别在于编译器去哪里寻找该文件：
//
//   #include "data_feed.h"   ← 双引号：优先在本项目的源代码目录中查找
//     用于包含自己写的文件（同一项目内的头文件）
//     找不到时才会继续搜索系统目录
//
//   #include <vector>        ← 尖括号：直接在系统/标准库目录中查找
//     用于包含 C++ 标准库或第三方库提供的头文件
//     不会搜索本项目目录
//
// 简单记忆：
//   双引号 → 自己写的文件
//   尖括号 → C++ 内置或外部库的文件
#include "data_feed.h"
#include <vector>

// ─────────────────────────────────────────────────────────────
// 函数声明（Declaration）与函数实现（Definition）的区别
// ─────────────────────────────────────────────────────────────
//
// 这一行是"声明"：
//   void runEngine(const std::vector<Tick> &ticks);
//   → 只告诉编译器"有这样一个函数存在，它接受什么参数、返回什么类型"
//   → 没有函数体（没有 { ... }），以分号结尾
//   → 写在 .h 头文件中，供其他文件通过 #include 引用
//
// 真正的"实现"写在 engine.cpp 中：
//   void runEngine(const std::vector<Tick> &ticks) {
//       // 具体代码 ...
//   }
//   → 包含完整的函数体，告诉编译器"这个函数具体做什么"
//
// 为什么要分开？
//   声明（.h）= 目录 / 菜单：告诉外界"我提供什么"
//   实现（.cpp）= 厨房：具体怎么做，外界不需要关心
//
//   这样 main.cpp 只需要 #include "engine.h" 就能调用 runEngine，
//   而不需要知道它内部是怎么实现的。
//   未来修改实现细节时，也不影响任何调用它的文件。
// ─────────────────────────────────────────────────────────────
// runEngine：启动逐笔驱动的模拟循环
// 对每一条行情数据打印日期、涨跌方向（UP / DOWN / FLAT）和价格
// 循环持续运行，直到用户按下 Ctrl+C 终止程序
void runEngine(const std::vector<Tick> &ticks);
